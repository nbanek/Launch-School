let weather = 'sunny';

switch (weather) {
  case 'sunny':
    console.log(`It's a beautiful day!`);
    break;
  case 'rainy':
    console.log(`Grab an umbrella.`);
    break;
  case 'windy':
    console.log(`Bring your jacket.`);
    break;
  default:
    console.log(`Let's stay inside.`);
}